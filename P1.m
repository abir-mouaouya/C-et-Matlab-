% --- Executes on selection change in popupmenu4.
function popupmenu4_Callback(hObject, eventdata, handles)
% hObject    handle to popupmenu4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: contents = cellstr(get(hObject,'String')) returns popupmenu4 contents as cell array
%        contents{get(hObject,'Value')} returns selected item from popupmenu4
choix=get(handles.popupmenu4,'value');      % le choix de fonction 
N  = str2num(get(handles.edit11,'string'));  %nombre des echantillons 
Fe = str2num(get(handles.edit10,'string'));   % la frequence 
A  = str2num(get(handles.edit12,'string'));    % l'amplitude 

temps=(0:N-1)/Fe;
freq=-N/2:Fe/N:(N/2)-1;

F=400
switch choix 
      case 1
            signal=A*sin(2*pi*F*temps);
            titre='signal sinus';
      case 2 
            signal=A*cos(2*pi*F*temps);
            titre ='signal cosinus';
      case 3 
            signal=A*sawtooth(2*pi*F*temps);
            titre ='signal dent de scie';
      case 4
            signal=A*square(2*pi*F*temps);
            titre ='signal dent de scie';
      case 5
            signal =A*sinc(2*pi*temps*F);
            %signal=(temps==0) + sin(temps)./(temps+(temps==0)) ;
            titre ='signal sinuscardinal';
     case 6 
            signal=A*rectpuls(temps,1); 
            titre='signal rectangulaire';
     case 7
            signal=[1, zeros(1,N-1)];
            titre='Implulsion de DIRAC';
      
end 

  % dessiner le choix  du  signal 
   axes(handles.axes3);
   plot(signal,'b');
   grid  
   xlabel ('temps ') ;
   ylabel ('amplitude') ;
   title(titre); 

   %enregistrer les echantillons dans un fichier 
  MyFile=fopen('C:\Users\Abir\Desktop\miniprojet2\echantillon.txt','w');      % ouvrir le fichier en mode ecriture 
  fprintf(MyFile,'%f\n',signal);            %stocker le signal dans le fichier
  fclose(MyFile);                           %fermer le fichier 
   
   
   %calcul des grandeurs de signal 
   puissance=sum(signal.^2)/N;
   energie=sum(signal.^2);
   moyenne=mean(signal);
   
   %Affichage de grandeurs : 
   set(handles.text31,'string',puissance);
   set(handles.text32,'string',energie);
   set(handles.text33,'string',moyenne);
    
